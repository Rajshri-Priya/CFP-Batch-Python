# operators
# Arithmetic Operators
a = int(input("Enter value for a :"))
b = int(input("Enter value for b:"))

# addition
print('Sum: ', a + b)

# subtraction
print('Subtraction: ', a - b)

# multiplication
print('Multiplication: ', a * b)

# division
print('Division: ', a / b)

# modulo
print('Modulo: ', a % b)

# a to the power b
print('Power: ', a ** b)
